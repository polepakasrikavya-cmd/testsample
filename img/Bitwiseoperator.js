//Bitwise operator

//Bitwise AND
let a = 5; // 0101
let b = 3; // 0011
let c = a & b; // 0001
console.log(c); 

//Bitwise OR
let d = a | b; // 0111
console.log(d); 

//Bitwise XOR
let e = a ^ b; // 0110
console.log(e); 

//Bitwise NOT //X=-(x+1)
let f = ~a; // 1010
console.log(f);

//Left Shift
let g = a << 1; // 1010
console.log(g); 

//Right Shift
let h = a >> 1; // 0010
console.log(h);

//Unsigned Right Shift
let i = a >>> 1; // 0010
console.log(i); 


